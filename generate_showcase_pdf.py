import os
import re
from fpdf import FPDF

OUTDIR = 'sample-reports'
os.makedirs(OUTDIR, exist_ok=True)

# ── Wade palette ──
NAVY = (30, 25, 79)
ORANGE = (241, 90, 34)
PINK = (237, 54, 148)
TEAL = (39, 189, 190)
YELLOW = (228, 229, 23)
