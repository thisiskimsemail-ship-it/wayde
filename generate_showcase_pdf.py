def _section_heading(pdf, pw, text):
    """Render an H2-style section heading with orange left accent."""
    y = pdf.get_y()
    pdf.set_draw_color(*ORANGE)
    pdf.set_line_width(1.2)
    pdf.line(pdf.l_margin, y, pdf.l_margin, y + 9)
    pdf.set_font(pdf._bold, 'B', 14)
    pdf.set_text_color(*NAVY)
    pdf.set_x(pdf.l_margin + 5)
    pdf.cell(pw - 5, 8, text)
    pdf.ln(11)


